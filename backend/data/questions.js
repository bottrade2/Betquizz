/**
 * Returns 'count' random questions with ALL language data included.
 * The client picks the right language to display — enabling per-player language
 * and mid-game language switching.
 */
function getQuestions(theme, difficulty, count = 10) {
  const pool = questions[theme]?.[difficulty];
  if (!pool || pool.length === 0) return [];

  const shuffled = [...pool];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }

  return shuffled.slice(0, Math.min(count, shuffled.length)).map(q => ({
    question: q.question,
    options:  q.options,
    answer:   q.answer,
    pt:       q.pt  || null,
    es:       q.es  || null,
  }));
}

